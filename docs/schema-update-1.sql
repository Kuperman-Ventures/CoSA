-- ─── 3. timer_sessions ───────────────────────────────────────────────────────
-- One row per task session. Upserted on every state change.
-- PK is a UUID generated by the app (crypto.randomUUID()).
CREATE TABLE IF NOT EXISTS public.timer_sessions (
  id                      uuid        PRIMARY KEY,
  user_id                 uuid        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  task_instance_id        text        REFERENCES public.today_task_instances(id),
  task_name               text        NOT NULL DEFAULT '',
  track                   text        NOT NULL DEFAULT 'advisors',
  kpi_mapping             text        NOT NULL DEFAULT '',
  timer_state             text        NOT NULL DEFAULT 'Not Started',
  completion_type         text        CHECK (completion_type IN ('Done','Done + Outcome','Partial','Cancelled')),
  estimate_seconds        integer     NOT NULL DEFAULT 0,
  elapsed_seconds         integer     NOT NULL DEFAULT 0,
  pause_count             integer     NOT NULL DEFAULT 0,
  pause_duration_seconds  integer     NOT NULL DEFAULT 0,
  overrun_seconds         integer     NOT NULL DEFAULT 0,
  cancelled_seconds       integer     NOT NULL DEFAULT 0,
  outcome_achieved        boolean,
  definition_of_done      text        NOT NULL DEFAULT '',
  actual_completed        text        NOT NULL DEFAULT '',
  started_at              timestamptz,
  completed_at            timestamptz,
  created_at              timestamptz NOT NULL DEFAULT now(),
  updated_at              timestamptz NOT NULL DEFAULT now()
);
