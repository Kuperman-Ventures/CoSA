-- ─── 1. task_templates ───────────────────────────────────────────────────────
-- One row per task in the user's Task Library.
CREATE TABLE IF NOT EXISTS public.task_templates (
  id                          text        PRIMARY KEY,
  user_id                     uuid        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  name                        text        NOT NULL DEFAULT '',
  track                       text        NOT NULL DEFAULT 'advisors'
    CHECK (track IN ('advisors','jobSearch','ventures','networking','development','cosaAdmin')),
  time_block                  text        NOT NULL DEFAULT 'BD'
    CHECK (time_block IN ('BD','Networking','Job Search','Encore OS','Friday')),
  default_estimate_minutes    integer     NOT NULL DEFAULT 25,
  completion_type             text        NOT NULL DEFAULT 'Done'
    CHECK (completion_type IN ('Done','Done + Outcome','Partial')),
  status                      text        NOT NULL DEFAULT 'Active'
    CHECK (status IN ('Active','Paused','Archived')),
  requires_definition_of_done boolean     NOT NULL DEFAULT false,
  frequency                   text        NOT NULL DEFAULT 'Weekly'
    CHECK (frequency IN ('Daily','Weekly','Monthly','As scheduled')),
  kpi_mapping                 text        NOT NULL DEFAULT '',
  subtasks                    text        NOT NULL DEFAULT '',
  outcome_prompt              text        NOT NULL DEFAULT '',
  created_at                  timestamptz NOT NULL DEFAULT now(),
  updated_at                  timestamptz NOT NULL DEFAULT now()
);
ALTER TABLE public.task_templates ENABLE ROW LEVEL SECURITY;
DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE tablename = 'task_templates' AND policyname = 'task_templates_own_all'
  ) THEN
    CREATE POLICY "task_templates_own_all" ON public.task_templates FOR ALL
      USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);
  END IF;
END $$;

